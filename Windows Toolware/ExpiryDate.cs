using System;

namespace Windows_Toolware
{
    internal class ExpiryDate
    {
        internal static ExpiryDate GetExpiryDate()
        {
            return GetExpiryDate();
        }

        internal static ExpiryDate GetExpiryDate(int v)
        {
            return GetExpiryDate(v);
        }

        public static implicit operator string(ExpiryDate v)
        {
            return v;
        }
    }
}